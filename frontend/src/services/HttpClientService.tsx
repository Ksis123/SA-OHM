import React from "react";
import { ScholarshipInterface } from "../interfaces/iScholarship";
import { AdminsInterface } from "../interfaces/iAdmins";
import {StatusInterface} from "../interfaces/iStatus";

const apiUrl = "http://localhost:5050";




async function GetScholarship() {
    const requestOptions = {
      method: "GET",
      headers: {
        Authorization: `Bearer ${localStorage.getItem("token")}`,
        "Content-Type": "application/json",
      },
    };
    let res = await fetch(`${apiUrl}/scholarship`, requestOptions)
    .then((response) => response.json())
    .then((res) => {
      if (res.data) {
        return res.data;
      } else {
        return false;
      }
    });

  return res;
}

